def green(text):
    return "\033[1;32m" + text + "\033[1;m"

def red(text):
    return "\033[1;31m" + text + "\033[1;m"

def blue(text):
    return "\033[1;34m" + text + "\033[1;m"

def yellow(text):
    return "\033[1;33m" + text + "\033[1;m"

